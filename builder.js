const TARVEX_EMAIL = "tarvexnetworks@gmail.com";

let siteData = {}

function generate(){

let text = document.getElementById("prompt").value.toLowerCase()

if(text==""){
alert("Describe website first")
return
}

/* THEME AI */

let theme = {
bg:"#ffffff",
text:"#000000",
nav:"#0b2a5b",
card:"#f4f4f4"
}

if(text.includes("dark")){
theme = {
bg:"#0b0b0b",
text:"#ffffff",
nav:"#000000",
card:"#1a1a1a"
}
}

/* NAV */

let nav = `
<div style="background:${theme.nav};color:white;padding:20px">
<h2>${text.toUpperCase()}</h2>
<a href="#" onclick="loadPage('index')" style="color:white;margin-right:15px">Home</a>
<a href="#" onclick="loadPage('about')" style="color:white;margin-right:15px">About</a>
<a href="#" onclick="loadPage('services')" style="color:white;margin-right:15px">Services</a>
<a href="#" onclick="loadPage('contact')" style="color:white">Contact</a>
</div>
`

/* HERO AI */

let hero = `
<div style="padding:60px 20px;text-align:center">
<h1>Build Your Future</h1>
<p>${text}</p>
<button style="
padding:12px 20px;
background:${theme.nav};
color:white;
border:none;
border-radius:6px;
margin-top:10px
">
Get Started
</button>
</div>
`

/* FEATURES */

let features = `
<div style="padding:20px">

<h2>Features</h2>

<div style="display:flex;gap:15px;flex-wrap:wrap">

<div style="background:${theme.card};padding:20px;flex:1">
AI Powered
</div>

<div style="background:${theme.card};padding:20px;flex:1">
Fast Deployment
</div>

<div style="background:${theme.card};padding:20px;flex:1">
Modern Design
</div>

</div>

</div>
`

/* FOOTER */

let footer = `
<div style="
background:${theme.nav};
color:white;
padding:20px;
text-align:center
">

Tarvex AI Builder  
<br>
${TARVEX_EMAIL}

</div>
`

/* HOME */

siteData.index = `
${nav}

<div style="background:${theme.bg};color:${theme.text}">
${hero}
${features}
${footer}
</div>
`

/* ABOUT */

siteData.about = `
${nav}

<div style="padding:20px;background:${theme.bg};color:${theme.text}">

<h1>About</h1>
<p>This website was generated by Tarvex AI</p>

</div>

${footer}
`

/* SERVICES */

siteData.services = `
${nav}

<div style="padding:20px;background:${theme.bg};color:${theme.text}">

<h1>Services</h1>

<ul>
<li>Website Builder</li>
<li>AI Generation</li>
<li>Hosting</li>
<li>Domains</li>
</ul>

</div>

${footer}
`

/* CONTACT */

siteData.contact = `
${nav}

<div style="padding:20px;background:${theme.bg};color:${theme.text}">

<h1>Contact</h1>

<p>Email: ${TARVEX_EMAIL}</p>

</div>

${footer}
`

document.getElementById("result").style.display="block"

document.getElementById("result").innerHTML = `
<h3>Website Preview</h3>

<div id="preview" style="border-radius:10px;overflow:hidden">
${siteData.index}
</div>

<button onclick="deploy()">Deploy Website</button>
`

}

function loadPage(page){
document.getElementById("preview").innerHTML = siteData[page]
}

function deploy(){
localStorage.setItem("tarvex_site", JSON.stringify(siteData))
window.location.href="deploy.html"
}
