const TARVEX_EMAIL = "tarvexnetworks@gmail.com";

let generatedHTML = ""

function generate(){

let text = document.getElementById("prompt").value

if(text==""){
alert("Describe website first")
return
}

generatedHTML = `
<html>
<head>
<title>${text}</title>
</head>

<body style="font-family:Arial;margin:0">

<div style="background:#0b2a5b;color:white;padding:20px">
<h1>${text}</h1>
</div>

<div style="padding:20px">

<h2>Welcome</h2>
<p>This website was generated by Tarvex AI</p>

<h3>Services</h3>
<ul>
<li>Website Development</li>
<li>Domains</li>
<li>Hosting</li>
</ul>

<h3>Contact</h3>
<p>Email: ${TARVEX_EMAIL}</p>

</div>

</body>
</html>
`

document.getElementById("result").style.display="block"

document.getElementById("result").innerHTML = `

<h3>Website Preview</h3>

<iframe style="
width:100%;
height:500px;
border:none;
border-radius:10px;
background:white;
"
srcdoc='${generatedHTML}'
></iframe>

<button onclick="deploy()">Deploy Website</button>

`

}

function deploy(){

localStorage.setItem("tarvex_site", generatedHTML)

window.location.href="deploy.html"

}
